package com.example.listita;

public class calculos
{
    public double suma (int num1, int num2)
    {
        int total = 0;
        total = num1 + num2;
        return total;
    }
    public double resta (int num1, int num2)
    {
        int total = 0;
        total = num1 - num2;
        return total;
    }
    public double multi (int num1, int num2)
    {
        int total = 0;
        total = num1 * num2;
        return total;
    }
    public double divide (int num1, int num2)
    {
        int total = 0;
        total = num1 / num2;
        return total;
    }
}
